    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        
        
        String ad=kitapadi_alan.getText();
        String yazar=kitapyazar_alan.getText();
        String baski=kitapbaski_alan.getText();
        String sayfa=kitapsayfa_alan.getText();
        String dil=kitapdil_alan.getText();
        String yayınevi=kitapyayin_alan.getText();
        
        int selectedRow = tablo.getSelectedRow();
         if (selectedRow == -1) {
            
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("Çalışanlar Tablosu şu anda boş. ");
            }
            else {
                mesaj_yazisi.setText("Lütfen güncellenecek bir çalışan seçin.");
            }
        }
        else {
            int no = (int)model.getValueAt(selectedRow,6);
            islem.kitapGuncelle(ad,yazar,baski,sayfa,dil,yayınevi,no);
            kitap_goruntule();
            mesaj_yazisi.setText("Çalışan başarıyla güncellendi...");
        }
    }//GEN-LAST:event_guncelleActionPerformed

    private void kitapSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kitapSilActionPerformed
       mesaj_yazisi.setText("");
        
       int selectedrow =tablo.getSelectedRow();
       
       if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               mesaj_yazisi.setText("Çalışan tablosu şu anda boş...");
           }
           else {
               mesaj_yazisi.setText("Lütfen silinecek bir çalışan seçin...");
           }
      
       }
       else {
           int no = (int)model.getValueAt(selectedrow,6);
           
           islem.kitapSil(no);
           
           kitap_goruntule();
           
           mesaj_yazisi.setText("Çalışan başarıyla silindi...");
           
           
           
           
       }
        
    }//GEN-LAST:event_kitapSilActionPerformed

