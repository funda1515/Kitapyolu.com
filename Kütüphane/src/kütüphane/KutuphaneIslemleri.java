    public void kitap_goruntule(){
        model.setRowCount(0);
        
        ArrayList<KitapProperties> kitapOzellik = new ArrayList<KitapProperties>();
        kitapOzellik = islem.kitaplari_getir();
        
        if(kitapOzellik != null){
            for (KitapProperties kp: kitapOzellik ){
                Object [] eklenecek = {kp.getKitapAd(),kp.getKitapYazari(),kp.getKitapBaskiYil(),kp.getKitapSayfaSayisi(),kp.getKitapDil(),kp.getKitapYayinEvi(),kp.getKitapNo()};
                
                model.addRow(eklenecek);
            }
            
        }
    }
   
